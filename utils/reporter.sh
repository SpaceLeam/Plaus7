#!/bin/bash
#
# Reporter
# Generates HTML, JSON, and Markdown reports
#

set -euo pipefail

INPUT_DIR="$1"
OUTPUT_DIR="$2"
TARGET="$3"

if [[ -z "$INPUT_DIR" ]] || [[ -z "$OUTPUT_DIR" ]] || [[ -z "$TARGET" ]]; then
    echo "Usage: $0 <input_dir> <output_dir> <target>"
    exit 1
fi

mkdir -p "$OUTPUT_DIR"

TIMESTAMP=$(date -Iseconds)
DATE_HUMAN=$(date '+%Y-%m-%d %H:%M:%S')

# Gather stats
SUBDOMAINS=0
ALIVE_HOSTS=0
OPEN_PORTS=0
VULNS=0

[[ -f "$INPUT_DIR/raw/subdomains.txt" ]] && SUBDOMAINS=$(wc -l < "$INPUT_DIR/raw/subdomains.txt" 2>/dev/null || echo 0)
[[ -f "$INPUT_DIR/raw/alive_urls.txt" ]] && ALIVE_HOSTS=$(wc -l < "$INPUT_DIR/raw/alive_urls.txt" 2>/dev/null || echo 0)
[[ -f "$INPUT_DIR/raw/ports.json" ]] && OPEN_PORTS=$(grep -c '"open": true' "$INPUT_DIR/raw/ports.json" 2>/dev/null || echo 0)
[[ -f "$INPUT_DIR/vulnerabilities/nuclei.json" ]] && VULNS=$(grep -c '"template"' "$INPUT_DIR/vulnerabilities/nuclei.json" 2>/dev/null || echo 0)

echo "[reporter] Generating reports for $TARGET"

# =============================================================================
# JSON REPORT
# =============================================================================

cat > "$OUTPUT_DIR/report.json" << EOF
{
  "target": "$TARGET",
  "timestamp": "$TIMESTAMP",
  "statistics": {
    "subdomains_found": $SUBDOMAINS,
    "live_hosts": $ALIVE_HOSTS,
    "open_ports": $OPEN_PORTS,
    "vulnerabilities": $VULNS
  },
  "files": {
    "subdomains": "raw/subdomains.txt",
    "alive_urls": "raw/alive_urls.txt",
    "ports": "raw/ports.json",
    "vulnerabilities": "vulnerabilities/nuclei.json"
  }
}
EOF

# =============================================================================
# MARKDOWN REPORT
# =============================================================================

cat > "$OUTPUT_DIR/report.md" << EOF
# Reconnaissance Report: $TARGET

**Generated:** $DATE_HUMAN

---

## Summary

| Metric | Count |
|--------|-------|
| Subdomains Found | $SUBDOMAINS |
| Live Hosts | $ALIVE_HOSTS |
| Open Ports | $OPEN_PORTS |
| Vulnerabilities | $VULNS |

---

## Subdomains

EOF

if [[ -f "$INPUT_DIR/raw/subdomains.txt" ]] && [[ -s "$INPUT_DIR/raw/subdomains.txt" ]]; then
    echo '```' >> "$OUTPUT_DIR/report.md"
    head -50 "$INPUT_DIR/raw/subdomains.txt" >> "$OUTPUT_DIR/report.md"
    if [[ $SUBDOMAINS -gt 50 ]]; then
        echo "... and $((SUBDOMAINS - 50)) more" >> "$OUTPUT_DIR/report.md"
    fi
    echo '```' >> "$OUTPUT_DIR/report.md"
else
    echo "_No subdomains found_" >> "$OUTPUT_DIR/report.md"
fi

cat >> "$OUTPUT_DIR/report.md" << EOF

---

## Live Hosts

EOF

if [[ -f "$INPUT_DIR/raw/alive_urls.txt" ]] && [[ -s "$INPUT_DIR/raw/alive_urls.txt" ]]; then
    echo '```' >> "$OUTPUT_DIR/report.md"
    head -30 "$INPUT_DIR/raw/alive_urls.txt" >> "$OUTPUT_DIR/report.md"
    if [[ $ALIVE_HOSTS -gt 30 ]]; then
        echo "... and $((ALIVE_HOSTS - 30)) more" >> "$OUTPUT_DIR/report.md"
    fi
    echo '```' >> "$OUTPUT_DIR/report.md"
else
    echo "_No live hosts found_" >> "$OUTPUT_DIR/report.md"
fi

cat >> "$OUTPUT_DIR/report.md" << EOF

---

## Vulnerabilities

EOF

if [[ -f "$INPUT_DIR/vulnerabilities/nuclei.json" ]] && [[ $VULNS -gt 0 ]]; then
    if command -v jq &> /dev/null; then
        jq -r '.[] | "- **\(.info.severity | ascii_upcase)**: \(.info.name) at \(.host)"' \
            "$INPUT_DIR/vulnerabilities/nuclei.json" 2>/dev/null | head -20 >> "$OUTPUT_DIR/report.md" || true
    else
        echo "See \`vulnerabilities/nuclei.json\` for details" >> "$OUTPUT_DIR/report.md"
    fi
else
    echo "_No vulnerabilities found_" >> "$OUTPUT_DIR/report.md"
fi

cat >> "$OUTPUT_DIR/report.md" << EOF

---

_Report generated by Smart Recon Automation Suite 2025_
EOF

