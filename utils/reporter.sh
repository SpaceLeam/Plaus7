    head -50 "$INPUT_DIR/raw/alive_urls.txt" | while read -r line; do
        echo "                <div><a href=\"$line\" target=\"_blank\" style=\"color: var(--accent);\">$line</a></div>" >> "$OUTPUT_DIR/report.html"
    done
else
    echo "                <div>No live hosts found</div>" >> "$OUTPUT_DIR/report.html"
fi

cat >> "$OUTPUT_DIR/report.html" << 'HTMLFOOT'
            </div>
        </div>
        
